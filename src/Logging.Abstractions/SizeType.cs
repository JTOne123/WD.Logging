namespace WD.Logging.Abstractions
{
    /// <summary>
    /// Size types
    /// </summary>
    public enum SizeType
    {
        /// <summary>
        /// Bytes
        /// </summary>
        Byte = 0,
        /// <summary>
        /// Kilobyte (1000 bytes)
        /// </summary>
        KB = 1,
        /// <summary>
        /// Kilobyte (1000 bytes)
        /// </summary>
        KiloByte = 1,
        /// <summary>
        /// Kibibyte (1024 bytes)
        /// </summary>
        KiBi = 2,
        /// <summary>
        /// Kibibyte (1024 bytes)
        /// </summary>
        KibiByte = 2,
        /// <summary>
        /// Megabyte (1000 factors)
        /// </summary>
        MB = 3,
        /// <summary>
        /// Megabyte (1000 factors)
        /// </summary>
        MegaByte = 3,
        /// <summary>
        /// Mibibyte (1024 factors)
        /// </summary>
        MiBi = 4,
        /// <summary>
        /// Mibibyte (1024 factors)
        /// </summary>
        MibiByte = 4,
    }
}
